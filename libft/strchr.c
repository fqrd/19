char	*ft_strchr(const char *s, int c)
{
	int i;
	char *p = (char *)s;

	i = 0;
	while (p[i] != '\0')
	{
		if (p[i] == c)
			return ((char *)s);
		i++;
		s++;
	}
	if (p[i] == c)
		return ((char *)s);
	return (0);
}
