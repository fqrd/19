#include <stddef.h>

void	*ft_memmove(void *dest, const void *src, size_t n)
{
	const char	*p;
	char		*d;

	p = (char *)src;
	d = (char *)dest;
	while (n)
	{
		*d = *p++;
		d++;
		n--;
	}
	return (dest);
}
